void etoa(d,s,len)		/* ebcdic to ascii */
char *d;
const unsigned char *s;
int len;
{
    while(len-- > 0) *d++ = etoatbl[*s++];
}

void atoe(d,s,len)		/* ascii to ebcdic */
char *d;
const char *s;
int len;
{
    while (len-- > 0) *d++ = atoetbl[*s++ & 0x7F];
}
